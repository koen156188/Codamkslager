#include <unistd.h>
#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (s != NULL)
	{
		write(fd, s, ft_strlen((const char *)s));
		ft_putchar_fd('\n', fd);
	}
}
